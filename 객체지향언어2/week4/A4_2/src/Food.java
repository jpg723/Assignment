
class Food {
	private int cal, cost, kg;
	
	public Food(int ca, int c, int k) {
		this.cal = ca;
		this.cost = c;
		this.kg = k;
	}
	public int getCal() {
		return cal;
	}
	public void setCal(int cal) {
		this.cal = cal;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	public int getKg() {
		return kg;
	}
	public void setKg(int kg) {
		this.kg = kg;
	}

}
