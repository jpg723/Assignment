
public class Phone {

	int idNum;
	String name;
	String phoneNumber; // 문자열 변수 phoneNumber
	String email; // 문자열 변수 address

	public Phone(int idNum, String name, String phoneNumber, String email) {

		this.idNum = idNum;
		this.name = name;
		this.phoneNumber = phoneNumber;
		this.email = email;
	}
}


