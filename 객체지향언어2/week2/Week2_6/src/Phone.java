
class Phone {
	private String name;
	private String tel;
	
	public Phone(String n, String t) {
		name = n;
		t = tel;
		
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getTel() {
		return tel;
	}

}
